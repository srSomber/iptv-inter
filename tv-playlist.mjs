function fetchUrl(url) {
  return new Promise((resolve, reject) => {
    const client = url.startsWith('https') ? https : http;
    client.get(url, res => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => resolve(data));
    }).on('error', reject);
  });
}

function normalizeName(name) {
  return name.replace(/\s+/g, ' ').trim();
}

function findChannelKey(name) {
  const normalized = normalizeName(name);
  if (nameToKey[normalized]) return nameToKey[normalized];
  const altKey = name.replace(/[\s\-]/g, '_').replace(/_+/g, '_');
  if (channelLogos[altKey]) return altKey;
  return null;
}

function isEventChannel(name) {
  const n = name.toLowerCase();
  return (n.includes('eventos') || n.includes('evento')) && /\d/.test(name);
}

async function generateTVPlaylist() {
  const html = await fetchUrl(WATCH_URL);

  const match = html.match(/var Streams\s*=\s*(\[[\s\S]*?\]);/);
  if (!match) {
    process.stderr.write('Could not find Streams data in the page');
    process.exit(1);
  }

  const streams = JSON.parse(match[1]);

  const m3uLines = [
    '#EXTM3U',
    `#PLAYLIST: TvPlay Canales (${streams.length})`,
    ''
  ];

  for (const ch of streams) {
    const chKey = findChannelKey(ch.name);
    const logoUrl = isEventChannel(ch.name) ? null : (channelLogos[chKey] || null);

    let logoAttr = '';
    if (logoUrl) {
      logoAttr = ` tvg-logo="${logoUrl}"`;
    } else if (ch.poster) {
      logoAttr = ` tvg-logo="${BASE_URL}/${ch.poster}"`;
    }

    m3uLines.push(`#EXTINF:-1 tvg-id="${ch.m3u8}" tvg-name="${ch.name}"${logoAttr},${ch.name}`);
    m3uLines.push(ch.url);
    m3uLines.push('');
  }

  const output = m3uLines.join('\n');
  process.stdout.write(output);
}

generateTVPlaylist().catch(err => {
  console.error('Error:', err.message);
  process.exit(1);
});
